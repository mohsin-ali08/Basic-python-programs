

# 2. Linear Equation (Basic Arithmetic)

a = float(input("\nEnter value of a: "))
b = float(input("Enter value of b: "))

x = -b / a

print(f"\nValue of x is:{x}\n")


# 🔹 Explanation:
# This program solves a simple linear equation of the form ax + b = 0. We use the formula x = -b / a.
# User enters values of a and b, and the program calculates the value of x.
